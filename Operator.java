class Operator
	{
		public static void main(String[]args)
		{
			int n1=20,n2=30;
			char ope='+';
			if(ope=='+')
			{
				System.out.println(n1+n2);
			}
			if(ope=='-')
			{
				System.out.println(n1-n2);
}
			if(ope=='*')
			{
				System.out.println(n1*n2);
}
	                if(ope=='/')
			{
				System.out.println(n1/n2);
}
		}
	}